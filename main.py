def norm_1(M):
    """
    Считаем сумму в каждом стоблце, выбирраем максимальную
    (Сервер на который пришлась самая большая нагрузка)
    
    """
    return max(sum(abs(M[i, j]) for i in range(M.shape[0])) for j in range(M.shape[1]))

def norm_inf(M):
    """
    Считаем сумму в каждой строке и выбираем максимальную
    (Сервер от которого больше всего исходящего трафика)
    """
    return max(sum(abs(M[i, j]) for j in range(M.shape[1])) for i in range(M.shape[0]))

def norm_frobenius(M):
    """
    Все элементы возводим в квадрат и суммируем, после извлекаем корень
    (Аналого евклидовой длины для векторы, измеряем суммарную энергию(массу))
    """

    return np.sqrt(sum(M[i, j]**2 for i in range(M.shape[0]) for j in range(M.shape[1])))

def condition_number(M):
    """
    Раскладывает матрицу и находит ее сигнатурные числа (Массив S),
    Отбрасывает нулевые значения и берет самое большое сигнатурное число, после делит
    его на самое маленькое ненулевое.

    (Показывает насколько система уравнений чувствительна к погрешностям во входных данных.
    Большое число означает плохую обусловленность.) 
    """
    _, S, _ = np.linalg.svd(M)
    nonzero_s = S[S > 1e-12]
    return S[0] / nonzero_s[-1] if len(nonzero_s) > 0 else float('inf')

def power_iteration(M, tol=1e-8, max_iter=1000):
    """
    Степенной метод поиска собственных чисел

    1.Создает случайный начальный вектор v и делит его на его же длину (нормирует), чтобы он стал единичным.
    2. Запускает цикл: умножает матрицу M на текущий вектор v (получает вектор w).
    3. Находит новое приближение собственного числа через частное Рэлея
    4. Проверяет: если lambda изменилась по сравнению с прошлым шагом меньше чем на погрешность tol 10^{-8}, цикл останавливается.
    5. Если нет — вектор w нормируется, становится новым v, и круг повторяется.

    ( Находит самое большое по модулю собственное число матрицы lambda_{max} и соответствующий ему собственный вектор.)
    """
    n = M.shape[0]
    np.random.seed(42)
    v = np.random.rand(n)
    v /= np.linalg.norm(v)
    
    lambda_old = 0.0
    history = []
    
    for _ in range(max_iter):
        w = M @ v
        norm_w = np.linalg.norm(w)
        if norm_w < 1e-12:
            break
        v_next = w / norm_w
        lambda_cur = v_next.T @ M @ v_next
        history.append(lambda_cur)
        if abs(lambda_cur - lambda_old) < tol:
            return lambda_cur, v_next, history
        lambda_old = lambda_cur
        v = v_next
        
    return lambda_old, v, history

def inverse_iteration(M, mu=0.0, tol=1e-8, max_iter=1000):
    """
    Обратная итерация со сдвигом.
    
    МАТЕМАТИЧЕСКИЙ СМЫСЛ:
        Итерационный метод поиска собственного числа, БЛИЖАЙШЕГО к заданному сдвигу `mu`.
        Применяет степенной метод к обратной сдвинутой матрице (M - mu*I)^(-1).
        
    КАК РАБОТАЕТ В КОДЕ:
        1. Создает сдвинутую матрицу `B = M - mu * I`.
        2. Вместо дорогого и неустойчивого обращения матрицы `B`, на каждом шаге 
           цикла стабильно решает СЛАУ вида `B @ w = v` через функцию `np.linalg.solve`.
        3. Вычисляет значение `lambda` через частное Рэлея, проверяет сходимость по `tol`.
        4. Если точно попадает в собственное число (матрица `B` вырождена), перехватывает 
           ошибку `LinAlgError` и сразу возвращает `mu`.
           
    ПРИКЛАДНОЙ СМЫСЛ (СЕТЬ):
        Позволяет точечно исследовать конкретные диапазоны частот или нагрузок сети, 
        наводя «прицел» `mu` на интересующие нас области спектра. При `mu = 0` находит 
        самый ненагруженный («тихий») режим работы.
    """
    n = M.shape[0]
    np.random.seed(42)
    v = np.random.rand(n)
    v /= np.linalg.norm(v)
    
    I = np.eye(n)
    B = M - mu * I
    lambda_old = 0.0
    
    for idx in range(1, max_iter + 1):
        try:
            w = np.linalg.solve(B, v)
        except np.linalg.LinAlgError:
            return mu, v, idx
            
        v_next = w / np.linalg.norm(w)
        lambda_cur = v_next.T @ M @ v_next
        
        if abs(lambda_cur - lambda_old) < tol:
            return lambda_cur, v_next, idx
        lambda_old = lambda_cur
        v = v_next
        
    return lambda_old, v, max_iter

def gram_schmidt(vectors):
    """
    НАЗВАНИЕ: Процесс ортогонализации Грама-Шмидта.
    
    МАТЕМАТИЧЕСКИЙ СМЫСЛ:
        Построение ортонормированного (взаимно перпендикулярного) базиса 
        из произвольного набора линейно независимых векторов.
        
    КАК РАБОТАЕТ В КОДЕ:
        1. Берет очередной вектор `v`.
        2. Во внутреннем цикле вычитает из него проекции на все *уже выпрямленные* базисные векторы: `w -= (v @ b) * b`.
        3. Если после вычитания длина вектора `norm(w)` больше нуля (вектор независим), 
           он делися на свою длину (нормируется) и добавляется в базис.
        4. Если вектор коллинеарен (зависим), он зануляется и отбрасывается.
        
    ПРИКЛАДНОЙ СМЫСЛ (СЕТЬ):
        Очищает направления нагрузок от взаимного влияния и дублирования, 
        создавая систему независимых координат для анализа серверов.
    """
    basis = []
    for v in vectors.T:
        w = v.copy()
        for b in basis:
            w -= (v @ b) * b
        norm_w = np.linalg.norm(w)
        if norm_w > 1e-10:
            basis.append(w / norm_w)
    return np.array(basis).T

def change_of_basis(M, eigvecs):
    """
    НАЗВАНИЕ: Преобразование подобия матрицы (Смена базиса).
    
    МАТЕМАТИЧЕСКИЙ СМЫСЛ:
        D = P^(-1) @ M @ P = P^T @ M @ P (для ортогональной P).
        Перевод матрицы в базис её собственных векторов. Матрица `D` становится диагональной.
        
    КАК РАБОТАЕТ В КОДЕ:
        1. Прогоняет сырые собственные векторы через `gram_schmidt`, получая 
           идеальную ортогональную матрицу перехода `P`.
        2. Вычисляет трансформированную матрицу `D = P.T @ M @ P`.
        
    ПРИКЛАДНОЙ СМЫСЛ (СЕТЬ):
        Взгляд на структуру сети под «идеальным углом», где полностью отсутствуют 
        перекрестные помехи между каналами связи, а чистые независимые нагрузки 
        серверов лежат строго на главной диагонали матрицы `D`.
    """
    P = gram_schmidt(eigvecs)
    D = P.T @ M @ P
    return P, D

def qr_decomposition(M):

    Q = gram_schmidt(M)
    R = Q.T @ M
    return Q, R

def qr_algorithm(M, tol=1e-10, max_iter=500):
    """
    НАЗВАНИЕ: QR-алгоритм поиска всех собственных чисел.
    
    МАТЕМАТИЧЕСКИЙ СМЫСЛ:
        Итерационный метод вычисления полного спектра матрицы одновременно.
        Использует разложение матрицы на ортогональную (Q) и верхнетреугольную (R).
        
    КАК РАБОТАЕТ В КОДЕ:
        1. Берет текущую матрицу `A_k`.
        2. Раскладывает её: `Q, R = qr_decomposition(A_k)` (на базе Грама-Шмидта).
        3. Перемножает их в обратном порядке: `A_k = R @ Q`.
        4. С каждой итерацией элементы под главной диагональю стремятся к нулю.
        5. Проверяет зануление нижнего треугольника через норму Фробениуса: `np.tril(A_k, k=-1)`.
        6. Возвращает значения с главной диагонали матрицы.
        
    ПРИМЕЧАНИЕ ДЛЯ НЕСТИММЕТРИЧНЫХ МАТРИЦ:
        Без комплексных сдвигов алгоритм сходится к блочно-треугольной форме Шура. 
        Комплексно-сопряженные числа остаются "запертыми" внутри блоков 2x2 на диагонали, 
        поэтому ручной метод выводит вещественные компоненты этих блоков, а не сами комплексные корни.
    """
    A_k = M.copy()
    n = M.shape[0]
    for _ in range(max_iter):
        Q, R = qr_decomposition(A_k)
        A_k = R @ Q
        subdiag = np.linalg.norm(np.tril(A_k, k=-1), 'fro')
        if subdiag < tol:
            break
    return np.diag(A_k)

def build_laplacian(M):
    """
    НАЗВАНИЕ: Построение матрицы Лапласа графа (Лапласиан).
    
    МАТЕМАТИЧЕСКИЙ СМЫСЛ:
        L = D - A_sym
        Где A_sym — симметризованная матрица смежности, а D — диагональная матрица степеней.
        Лапласиан описывает топологическую структуру связей сети, абстрагируясь от направления трафика.
        
    КАК РАБОТАЕТ В КОДЕ:
        1. Усредняет матрицу с её транспонированной версией для симметрии: `A_sym = (M + M.T) / 2`.
        2. Считает сумму элементов в каждой строке `A_sym` (сколько каналов связи у узла) 
           и формирует диагональную матрицу степеней `D`.
        3. Находит Лапласиан: `L = D - A_sym`.
        
    ПРИКЛАДНОЙ СМЫСЛ (СЕТЬ):
        Фундамент для анализа устойчивости. Спектр матрицы L всегда вещественен и неотрицателен, 
        причем первое собственное число `lambda_1` всегда строго равно нулю. Второе наименьшее 
        число `lambda_2` — это «алгебраическая связность» сети.
    """
    A_sym = (M + M.T) / 2
    D = np.diag(np.sum(A_sym, axis=1))
    L = D - A_sym
    return A_sym, D, L

def fiedler_vector(L, mu=0.01, tol=1e-10, max_iter=500):
    """
    НАЗВАНИЕ: Вычисление Вектора Фидлера графа.
    
    МАТЕМАТИЧЕСКИЙ СМЫСЛ:
        Поиск собственного вектора матрицы Лапласа, соответствующего второму 
        наименьшему собственному числу lambda_2 (алгебраической связности).
        
    КАК РАБОТАЕТ В КОДЕ:
        1. Модифицирует метод обратной итерации, задавая микро-сдвиг `mu = 0.01` 
           (чтобы «прицелиться» чуть выше гарантированного нуля lambda_1).
        2. Внутри цикла решает СЛАУ, а затем выполняет КРИТИЧЕСКУЮ операцию:
           `w -= (w @ u1) * u1` — принудительно очищает вектор от проекции на тривиальный 
           единичный вектор `u1`, соответствующий нулю. Это заставляет алгоритм не сваливаться 
           к первому корню (0), а сходиться строго ко второму (`lambda_2`).
           
    ПРИКЛАДНОЙ СМЫСЛ (СЕТЬ):
        Выдаёт вектор Фидлера. Знаки его компонент («плюс» или «минус») разделяют серверы 
        на два независимых кластера. Это математическая карта того, по какому именно шву 
        развалится корпоративная сеть на изолированные сегменты, если критический узел-мост упадёт.
    """
    n = L.shape[0]
    np.random.seed(42)
    v = np.random.rand(n)
    v /= np.linalg.norm(v)
    
    u1 = np.ones(n) / np.sqrt(n)
    I = np.eye(n)
    B = L - mu * I
    lambda_old = 0.0
    
    for _ in range(max_iter):
        w = np.linalg.solve(B, v)
        w -= (w @ u1) * u1  
        
        v_next = w / np.linalg.norm(w)
        lambda_cur = v_next.T @ L @ v_next
        
        if abs(lambda_cur - lambda_old) < tol:
            return lambda_cur, v_next
        lambda_old = lambda_cur
        v = v_next
        
    return lambda_old, v


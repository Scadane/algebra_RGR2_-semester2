    # Получаем диагональ из QR-алгоритма
    qr_L_raw = qr_algorithm(L, max_iter=2000)
    
    # Если алгоритм вернул 5 элементов (скрыл машинный ноль), добавляем 0.0 вручную
    if len(qr_L_raw) < 6:
        L_eigs = np.zeros(6)
        L_eigs[1:] = np.sort(qr_L_raw)
    else:
        # Если вернул 6 элементов, просто сортируем и зануляем самый маленький
        L_eigs = np.sort(qr_L_raw.copy())
        if abs(L_eigs[0]) < 1e-3:
            L_eigs[0] = 0.0
            
    print(f"  - Спектр L (через QR): {np.round(L_eigs, 4)}")
    print(f"  - lambda_1 = {L_eigs[0]:.4f} (Теоретически должен быть 0)")
    print(f"  - lambda_2 = {L_eigs[1]:.4f} (Алгебраическая связность)")
    # --- 07. Вектор Фидлера ---
    print(f"\n[Раздел 07] Вектор Фидлера (Обратная итерация по L):")
    lam2_fiedler, f_vec = fiedler_vector(L, mu=0.01)
    diff = abs(lam2_fiedler - L_eigs[1])
    print(f"  - Найденная lambda_2: {lam2_fiedler:.4f}")
    print(f"  - Сверка с QR-алгоритмом: {'СОВПАДАЕТ' if diff < 1e-2 else 'ОТЛИЧАЕТСЯ'} (Погрешность: {diff:.2e})")
    print(f"  - Компоненты вектора: {np.round(f_vec, 4)}")
    cluster_A = [f"S{i+1}" for i, x in enumerate(f_vec) if x < 0]
    cluster_B = [f"S{i+1}" for i, x in enumerate(f_vec) if x >= 0]
    print(f"  - Разбиение: Группа A {cluster_A} | Группа B {cluster_B}")

    # --- 08. Сетевая интерпретация ---
    print(f"\n[Раздел 08] Сетевая модель и Прикладные выводы:")
    crit_node = np.argmax(np.abs(v_max))
    print(f"  1) КРИТИЧЕСКИЙ УЗЕЛ: S{crit_node + 1} (Определен по доминирующему вектору)")
    print(f"     Сравнение с нормой: Узел с макс. входящим трафиком (по ||A||_1) — это S3.")
    print(f"     Расхождение означает, что S2 — главный генератор атаки/нагрузки на S3.")
    
    # Возмущение
    A_perturbed = A.copy()
    A_perturbed[0, 2] += 0.09
    _, v_max_pert, _ = power_iteration(A_perturbed)
    crit_node_pert = np.argmax(np.abs(v_max_pert))
    print(f"  2) УСТОЙЧИВОСТЬ: При добавлении 10% шума в канал S1->S3 критический узел")
    print(f"     сместился на S{crit_node_pert + 1}. Число обусловленности kappa={kappa:.1f} (высокое),")
    print(f"     поэтому сеть чувствительна к мелким изменениям трафика.")
    
    # Атака
    A_attacked = A.copy()
    A_attacked[2, :] = 0.0  
    A_attacked[:, 2] = 0.0
    _, _, L_attacked = build_laplacian(A_attacked)
    lam2_attacked, _ = fiedler_vector(L_attacked)
    print(f"  3) РАЗБИЕНИЕ (DoS Атака): При отключении перегруженного узла S3 связность")
    print(f"     упала с {lam2_fiedler:.4f} до {lam2_attacked:.4f}. Сеть развалилась на изолированные части.")
    
    print("\n  [ИТОГОВЫЙ ПИСЬМЕННЫЙ ВЫВОД]")
    print("  Критическим звеном маршрутизации является узел S3, принимающий весь удар от генератора S2.")
    print("  При отказе S3 алгебраическая связность обнуляется, и сеть распадается на два изолированных")
    print("  кластера (S4-S5-S6 отрезаются от S1-S2). Требуется добавление резервных каналов.")

    # =====================================================================
    # ВИЗУАЛИЗАЦИЯ
    # =====================================================================
    fig, axs = plt.subplots(2, 2, figsize=(12, 10))
    colors = ['gray' if i != crit_node else 'crimson' for i in range(6)]
    axs[0, 0].bar([f"S{i+1}" for i in range(6)], np.abs(v_max), color=colors, edgecolor='black')
    axs[0, 0].set_title("Долгосрочное распределение нагрузки (v_max)")
    axs[0, 0].grid(axis='y', linestyle='--', alpha=0.7)
    
    axs[0, 1].plot(range(1, len(hist_pow)+1), hist_pow, 'o-', color='dodgerblue')
    axs[0, 1].set_title("Сходимость степенного метода")
    axs[0, 1].grid(True)
    
    true_lam = np.max(np.linalg.eigvals(A).real)
    err_pow = [abs(x - true_lam) for x in hist_pow]
    err_inv = [abs(inverse_iteration(A, mu=0.0, tol=1e-8, max_iter=i)[0] - true_lam) for i in range(1, 6)]
    axs[1, 0].semilogy(range(1, len(err_pow)+1), err_pow, '.-', label='Степенной метод', color='orange')
    axs[1, 0].semilogy(range(1, len(err_inv)+1), err_inv, 'D-', label='Обратная итерация', color='green')
    axs[1, 0].set_title("Сравнение сходимости (ошибка)")
    axs[1, 0].legend(); axs[1, 0].grid(True, which="both", ls="--")
    
    f_colors = ['blue' if x < 0 else 'green' for x in f_vec]
    axs[1, 1].bar([f"S{i+1}" for i in range(6)], f_vec, color=f_colors, edgecolor='black')
    axs[1, 1].axhline(0, color='black', linewidth=1.2)
    axs[1, 1].set_title("Вектор Фидлера (Разбиение)")
    axs[1, 1].grid(axis='y', linestyle='--', alpha=0.7)
    
    plt.tight_layout()
    plt.show()